for i in range(5):
    print(i, end=',')

print()
print("*" * 20)

for i in range(1,11):
    print(i, end=',')

print()
print("*" * 20)

for i in range(1,21,2):
    print(i, end=',')

print()
print("*" * 20)

# reverse loop
for i in range(10,1,-1):
    print(i, end=',')

print()
print("-" * 20)

for i in range(1,6):
    print('*' * i)

print("-" * 20)

